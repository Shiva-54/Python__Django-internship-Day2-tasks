print("=== Task 3: How Python Works ===")

print("What is a Python interpreter?")
print("The Python interpreter is a program that reads my Python code line by line")
print("and turns it into actions the computer can actually do.")

print("How Python code gets executed step by step:")
print("1) I write my code in a .py file.")
print("2) The interpreter reads the first line and checks if it is valid.")
print("3) If the line is correct, it executes it and then moves to the next line.")
print("4) This keeps going from top to bottom until the program ends.")
print("5) If there is an error, the interpreter stops and shows an error message.")
